/**
 * Get the default language code for a state. Falls back to "en".
 */
export function getDefaultLanguage(stateCode) {
  const meta = stateMetadata[stateCode];
  if (!meta) return "en";
  const def = meta.languages.find((l) => l.default);
  return def ? def.code : "en";
}

/**
 * Get labels for the currently selected language, falling back to English.
 */
export function getLabels(langCode) {
  return uiLabels[langCode] || uiLabels.en;
}
